package A02包装类;

public class A01包装类Integer {
    public static void main(String[] args) {


        /**Integer默认值是null*/

        //字符串---->int(前提字符串是数字类型)
        System.out.println(Integer.parseInt("11"));
        //Integer---->字符串
        System.out.println(Integer.toString(new Integer(22)));
        //字符串---->Integer
        System.out.println(Integer.valueOf("33"));


        /***其他请看API*/




    }
}
