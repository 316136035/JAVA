public class A01if分支判断 {
    public static void main(String[] args) {

        int score = 8000;

        if ( score>=10000){
            System.out.println("A级");
        }
        if ( score<10000&&score>=8000){
            System.out.println("B级");
        }
        if ( score<8000&&score>=6000){
            System.out.println("C级");
        }
        if ( score<6000){
            System.out.println("D级");
        }









    }
}
